<h2>Steps:</h2>
<ol>
    <li>
        Make sure you have an <a href="https://developers.google.com/adwords/api/docs/guides/first-api-call#oauth2_client_id_and_client_secret"
        target="_blank">OAuth2 client ID and secret</a>, <a href="https://developers.google.com/adwords/api/docs/guides/first-api-call#developer_token" target="_blank">a developer token</a>,
        and <a href="https://developers.google.com/adwords/api/docs/guides/first-api-call#oauth2_access_and_refresh_tokens"
        target="_blank">a refresh token</a> ready. If not, follow <a href="https://developers.google.com/adwords/api/docs/guides/first-api-call#overview_of_the_process"
        target="_blank">these instructions</a> to get them.
    </li>
    <li>
        Copy your <a href="https://github.com/googleads/googleads-php-lib/blob/master/examples/AdWords/adsapi_php.ini"
        target="_blank"><code>adsapi_php.ini</code></a>, needed for the Google Ads API client library for PHP, to the
        root directory of this sample app.
    </li>
    <li>
        That's it! You can start using this sample app to either
        <a href="https://developers.google.com/adwords/api/docs/samples/php/basic-operations#get-all-campaigns-using-awql"><strong>get all campaigns using CampaignService</strong></a>
        or <a href="https://developers.google.com/adwords/api/docs/guides/reporting"><strong>download a report of your specified type</strong></a>.
    </li>
</ol>
